package main

import "fmt"

func main() {
	// Declare variables
	var str string
	var n, m int
	var nm float32

	// Assign values
	str = "Hello World"
	n = 10
	m = 20
	nm = 3.45

	fmt.Println("Value of str is: ", str)
	fmt.Println("The value of n is: ", n)
	fmt.Println("The value of m is: ", m)
	fmt.Println("The value of nm is: ", nm)
}
